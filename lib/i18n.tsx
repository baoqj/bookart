interface I18nContextType {
  language: Language
  setLanguage: (lang: Language) => void
  t: (key: TranslationKey, params?: Record<string, string | number>) => string
}

const I18nContext = createContext<I18nContextType | undefined>(undefined)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>("zh-CN")

  useEffect(() => {
    const saved = localStorage.getItem("bookart-language") as Language
    if (saved && LANGUAGES.find((l) => l.code === saved)) {
      setLanguageState(saved)
    }
  }, [])

  const setLanguage = (lang: Language) => {
    setLanguageState(lang)
    localStorage.setItem("bookart-language", lang)
  }

  const t = (key: TranslationKey, params?: Record<string, string | number>): string => {
    const lang = (language as keyof typeof translations) in translations ? language : "en"
    const translationsAny = translations as unknown as Record<string, Record<string, string>>
    let text = translationsAny[lang]?.[key] || translationsAny.en[key] || key
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v))
      })
    }
    return text
  }

  return <I18nContext.Provider value={{ language, setLanguage, t }}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error("useI18n must be used within I18nProvider")
  return context
}
